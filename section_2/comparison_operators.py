# comparison operators: >, >=, <, <=, ==, !=
# whenever we compare, the value will be a boolean data type
a = 3
b = 2

greater_than = a > b
greater_equal = a >= b
less_than = a < b
less_equal = a <= b
is_not_equal = a != b
is_equal = a == b

print('is greater than: ', greater_than)
print('is greater or equal to: ', greater_equal)
print('is less than: ', less_than)
print('is less or equal to: ', less_equal)
print('is not equal to: ', is_not_equal)
print('is equal to: ', is_equal)